int cmos_gc0308_init(unsigned char dev_addr)
{
    int i = 0;
    while (END_PARAMETER != RESET_TAB[i])
    {
        if (0 == write_camera_reg(dev_addr, RESET_TAB[i],RESET_TAB[i+1]))
        {
            return(0);
        }
        i+=2;
    }
    return(1);
}

